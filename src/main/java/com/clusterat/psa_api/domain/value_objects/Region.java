/**
 * Represents the geographical regions of a country.
 *
 * This enumeration is used to define commonly recognized regions,
 * typically for classification or organizational purposes.
 *
 * Enumerated values:
 * - NORTE: Represents the northern region.
 * - NORDESTE: Represents the northeastern region.
 * - CENTRO_OESTE: Represents the central-western region.
 * - SUDESTE: Represents the southeastern region.
 * - SUL: Represents the southern region.
 */
public enum Region {
    NORTE,
    NORDESTE,
    CENTRO_OESTE,
    SUDESTE,
    SUL
}
